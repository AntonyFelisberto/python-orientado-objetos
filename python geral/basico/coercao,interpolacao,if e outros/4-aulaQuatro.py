#TIPOS INT E FLOAT

#INT NUMEROS NEGATIVOS E POSITIVOS SEM . EM PYTHON É NOMEADO COMO int
print(1)
print(0)
print(-1)

#FLOAT NUMEROS NEGATIVOS E POSITIVOS COM . EM PYTHON É NOMEADO COMO float
print(1.1)
print(0.0)
print(-1.2)

